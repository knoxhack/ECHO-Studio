function sortedUnique(values: string[]): string[] {
  return Array.from(new Set(values)).sort((a, b) => a.localeCompare(b))
}

function diffIds(expected: string[], actual: string[]): { missing: string[]; extra: string[] } {
  const expectedSet = new Set(expected)
  const actualSet = new Set(actual)
  return {
    missing: expected.filter((id) => !actualSet.has(id)),
    extra: actual.filter((id) => !expectedSet.has(id))
  }
}

async function moduleLockStatus(
  projectPath: string,
  manifest: AddonManifest,
  plan: ProjectModulePlan,
  mode: DevWorkspaceMode
): Promise<DevModuleLockStatus> {
  const studioLockPath = join(projectPath, '.echo-studio', 'modules.lock.json')
  const runtimeLockPath = join(projectPath, 'src', 'generated', 'resources', 'META-INF', 'echo.modules.lock.json')
  const [studioLock, runtimeLock] = await Promise.all([
    readModuleLock(studioLockPath),
    readModuleLock(runtimeLockPath)
  ])
  const runtimeExpected = mode !== 'visual'
  const expectedModuleIds = sortedUnique(plan.closure.map((mod) => mod.id))
  const lockedModuleIds = sortedUnique(studioLock?.modules.map((mod) => mod.id) ?? [])
  const runtimeModuleIds = sortedUnique(runtimeLock?.modules.map((mod) => mod.id) ?? [])
  const studioDiff = diffIds(expectedModuleIds, lockedModuleIds)
  const runtimeDiff = diffIds(expectedModuleIds, runtimeModuleIds)
  const projectMatches = Boolean(
    studioLock &&
    studioLock.project.id === manifest.id &&
    studioLock.project.version === manifest.version
  )
  const runtimeProjectMatches = !runtimeExpected || Boolean(
    runtimeLock &&
    runtimeLock.project.id === manifest.id &&
    runtimeLock.project.version === manifest.version
  )
  const studioUpToDate = Boolean(studioLock) && projectMatches && studioDiff.missing.length === 0 && studioDiff.extra.length === 0
  const runtimeUpToDate = !runtimeExpected || Boolean(runtimeLock) && runtimeProjectMatches && runtimeDiff.missing.length === 0 && runtimeDiff.extra.length === 0
  return {
    schemaVersion: 'echo.studio.modules.lock.status.v1',
    studioLockPath: relativePath(projectPath, studioLockPath),
    runtimeLockPath: relativePath(projectPath, runtimeLockPath),
    studioExists: Boolean(studioLock),
    runtimeExists: Boolean(runtimeLock),
    runtimeExpected,
    upToDate: studioUpToDate && runtimeUpToDate,
    runtimeUpToDate,
    projectMatches,
    expectedModuleIds,
    lockedModuleIds,
    runtimeModuleIds,
    missingFromLock: studioDiff.missing,
    extraInLock: studioDiff.extra,
    missingFromRuntimeLock: runtimeDiff.missing,
    extraInRuntimeLock: runtimeDiff.extra,
    lockedProjectId: studioLock?.project.id,
    lockedProjectVersion: studioLock?.project.version,
    generatedAt: studioLock?.generatedAt
  }
}

async function moduleWorkspaceStatus(
  projectPath: string,
  manifest: AddonManifest,
  plan: ProjectModulePlan
): Promise<DevModuleWorkspaceStatus> {
  const workspacePath = join(projectPath, '.echo-studio', 'module-workspace.json')
  const workspace = await readModuleWorkspace(workspacePath)
  const expectedModuleIds = sortedUnique(plan.closure.map((mod) => mod.id))
  const mappedModuleIds = sortedUnique(workspace?.modules.map((mod) => mod.id) ?? [])
  const diff = diffIds(expectedModuleIds, mappedModuleIds)
  const projectMatches = Boolean(
    workspace &&
    workspace.project.id === manifest.id &&
    workspace.project.version === manifest.version
  )
  const upToDate = Boolean(workspace) && projectMatches && diff.missing.length === 0 && diff.extra.length === 0

  return {
    schemaVersion: 'echo.studio.modules.workspace.status.v1',
    path: relativePath(projectPath, workspacePath),
    exists: Boolean(workspace),
    upToDate,
    projectMatches,
    moduleCount: workspace?.moduleCount ?? 0,
    localModuleCount: workspace?.localModuleCount ?? 0,
    expectedModuleIds,
    mappedModuleIds,
    missingFromMap: diff.missing,
    extraInMap: diff.extra,
    generatedAt: workspace?.generatedAt
  }
}

function parseGradleProperties(text: string): Record<string, string> {
  const properties: Record<string, string> = {}
  for (const rawLine of text.split(/\r?\n/g)) {
    const line = rawLine.trim()
    if (!line || line.startsWith('#') || line.startsWith('!')) continue
    const index = line.search(/[:=]/)
    if (index < 0) continue
    const key = line.slice(0, index).trim()
    const value = line.slice(index + 1).trim()
    if (key) properties[key] = value
  }
  return properties
}

interface ToolchainProbe {
  javaAvailable: boolean
  javaVersion?: string
  javaMajorVersion?: number
  systemGradleAvailable: boolean
  systemGradleVersion?: string
}

let toolchainProbeCache: { at: number; value: ToolchainProbe } | null = null

function parseJavaVersion(output: string): { version?: string; majorVersion?: number } {
  const match = output.match(/(?:openjdk|java)\s+version\s+"?([0-9][^"\s]*)/i) ?? output.match(/\b([0-9]+(?:\.[0-9]+)+)\b/)
  const version = match?.[1]
  if (!version) return {}
  const parts = version.split('.').map((part) => Number.parseInt(part, 10))
  const majorVersion = parts[0] === 1 ? parts[1] : parts[0]
  return Number.isFinite(majorVersion) ? { version, majorVersion } : { version }
}

function parseGradleVersion(output: string): string | undefined {
  return output.match(/\bGradle\s+([0-9][^\s\r\n]*)/i)?.[1]
}

function probeCommand(command: string, args: string[], timeoutMs: number): Promise<{ ok: boolean; output: string }> {
  return new Promise((resolve) => {
    let child: ReturnType<typeof spawn>
    try {
      child = spawn(command, args, { windowsHide: true })
    } catch {
      resolve({ ok: false, output: '' })
      return
    }
    let output = ''
    let settled = false
    function finish(ok: boolean): void {
      if (settled) return
      settled = true
      clearTimeout(timeout)
      resolve({ ok, output })
    }
    const timeout = setTimeout(() => {
      child.kill()
      finish(false)
    }, timeoutMs)
    child.stdout?.on('data', (chunk) => { output += String(chunk) })
    child.stderr?.on('data', (chunk) => { output += String(chunk) })
    child.on('close', (code) => {
      finish(code === 0)
    })
    child.on('error', () => {
      finish(false)
    })
  })
}

async function systemToolchainProbe(): Promise<ToolchainProbe> {
  const now = Date.now()
  if (toolchainProbeCache && now - toolchainProbeCache.at < 30000) return toolchainProbeCache.value

  const gradleExecutable = process.platform === 'win32' ? 'gradle.cmd' : 'gradle'
  const [java, gradle] = await Promise.all([
    probeCommand('java', ['-version'], 2500),
    probeCommand(gradleExecutable, ['--version'], 2500)
  ])
  const javaVersion = java.ok ? parseJavaVersion(java.output) : {}
  const value: ToolchainProbe = {
    javaAvailable: java.ok,
    ...(javaVersion.version ? { javaVersion: javaVersion.version } : {}),
    ...(javaVersion.majorVersion ? { javaMajorVersion: javaVersion.majorVersion } : {}),
    systemGradleAvailable: gradle.ok,
    ...(gradle.ok && parseGradleVersion(gradle.output) ? { systemGradleVersion: parseGradleVersion(gradle.output) } : {})
  }
  toolchainProbeCache = { at: now, value }
  return value
}

async function toolchainStatus(projectPath: string, hasGradleWrapper: boolean): Promise<DevToolchainStatus> {
  const properties = parseGradleProperties(await readText(join(projectPath, 'gradle.properties')))
  const requiredJavaVersion = Number.parseInt(properties.java_language_version ?? '25', 10) || 25
  const probe = await systemToolchainProbe()
  const javaMeetsRequirement = Boolean(probe.javaMajorVersion && probe.javaMajorVersion >= requiredJavaVersion)
  const gradleAvailable = hasGradleWrapper || probe.systemGradleAvailable
  const issues = [
    !probe.javaAvailable ? 'Java is not available on PATH.' : '',
    probe.javaAvailable && !javaMeetsRequirement
      ? `Java ${probe.javaVersion ?? 'version'} is below the generated target ${requiredJavaVersion}.`
      : '',
    !gradleAvailable ? 'No generated Gradle launcher or system Gradle was found.' : ''
  ].filter(Boolean)

  return {
    schemaVersion: 'echo.studio.toolchain.status.v1',
    requiredJavaVersion,
    ...(process.env.JAVA_HOME ? { javaHome: process.env.JAVA_HOME } : {}),
    javaAvailable: probe.javaAvailable,
    ...(probe.javaVersion ? { javaVersion: probe.javaVersion } : {}),
    ...(probe.javaMajorVersion ? { javaMajorVersion: probe.javaMajorVersion } : {}),
    javaMeetsRequirement,
    gradleWrapper: hasGradleWrapper,
    gradleAvailable,
    ...(probe.systemGradleVersion ? { gradleVersion: probe.systemGradleVersion } : {}),
    gradleCommand: hasGradleWrapper ? (process.platform === 'win32' ? '.\\gradlew.bat' : './gradlew') : 'gradle',
    issues
  }
}

async function runtimeLauncherStatus(
  projectPath: string,
  runtimes: Runtime[],
  mode: DevWorkspaceMode
): Promise<DevRuntimeLauncherStatus> {
  const gradlePropertiesPath = join(projectPath, 'gradle.properties')
  const gradlePropertiesExists = await exists(gradlePropertiesPath)
  const properties = parseGradleProperties(await readText(gradlePropertiesPath))
  const nativeExecutable = properties.echo_native_executable ?? ''
  const standaloneExecutable = properties.echo_standalone_executable ?? ''
  const nativeExpected = mode !== 'visual' && runtimes.includes('echo_native')
  const standaloneExpected = mode !== 'visual' && runtimes.includes('standalone')
  const nativeConfigured = nativeExecutable.trim().length > 0
  const standaloneConfigured = standaloneExecutable.trim().length > 0

  return {
    schemaVersion: 'echo.studio.runtime.launchers.status.v1',
    gradlePropertiesPath: relativePath(projectPath, gradlePropertiesPath),
    gradlePropertiesExists,
    nativeExpected,
    nativeConfigured,
    nativeExecutable,
    standaloneExpected,
    standaloneConfigured,
    standaloneExecutable,
    ready: (!nativeExpected || nativeConfigured) && (!standaloneExpected || standaloneConfigured)
  }
}

