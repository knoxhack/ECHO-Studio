function moduleTaskReadinessReason(state: DevWorkspaceState, label: string): string | null {
  if (!state.moduleLock.upToDate) return 'Refresh Dev Workspace so generated module locks match the current manifest.'
  if (!state.moduleWorkspace.upToDate) return 'Refresh Dev Workspace so local module source map matches the current manifest.'
  return moduleReadinessDisabledReason(state, `running ${label}`)
}

async function assertDevTaskReady(projectPath: string, task: (typeof DEV_TASKS)[number]): Promise<void> {
  const needsGradle = task.id.startsWith('gradle:')
  const needsPreviewRuntime = PREVIEW_RUNTIME_TASK_SET.has(task.id)
  const needsModuleReady = MODULE_READY_TASK_SET.has(task.id)
  if (!needsGradle && !needsPreviewRuntime && !needsModuleReady) return

  const state = await inspectDevWorkspace(projectPath)
  let reason: string | null = null
  if (needsPreviewRuntime) {
    reason = previewRuntimeDisabledReason(task.id, state, true)
  } else if (needsGradle) {
    if (!state.gradleReady) reason = 'Set up a Gradle workspace first.'
    else if (!state.toolchain.javaAvailable) reason = `Install Java ${state.toolchain.requiredJavaVersion} or add it to PATH.`
    else if (!state.toolchain.javaMeetsRequirement) reason = `Use Java ${state.toolchain.requiredJavaVersion} for this generated workspace.`
    else if (!state.toolchain.gradleAvailable) reason = 'Run Dev Workspace setup to generate the pinned Gradle launcher or install Gradle.'
  }
  if (!reason && needsModuleReady) reason = moduleTaskReadinessReason(state, task.label)
  if (reason) throw new Error(reason)
}

function className(manifest: AddonManifest): string {
  return localId(manifest)
    .split(/[_-]/g)
    .filter(Boolean)
    .map((part) => `${part[0]?.toUpperCase() ?? ''}${part.slice(1)}`)
    .join('') + 'Module'
}

function packagePath(manifest: AddonManifest): string {
  return `studio/generated/${manifest.namespace.replace(/[^a-z0-9_]/gi, '').toLowerCase()}/${localId(manifest).replace(/[^a-z0-9_]/gi, '').toLowerCase()}`.replace(/\//g, '/')
}

function artifactKind(name: string): DevArtifact['kind'] {
  if (name.endsWith('.echo-addon')) return 'echo-addon'
  if (name.endsWith('.jar')) return 'jar'
  if (name.endsWith('.json')) return 'manifest'
  if (name.endsWith('.sha256')) return 'checksum'
  return 'other'
}

function relativePath(from: string, to: string): string {
  return relative(from, to).replace(/\\/g, '/')
}

function safeTaskName(taskId: DevTaskId): string {
  return taskId.replace(/[^a-z0-9_-]/gi, '-')
}

function logStamp(date = new Date()): string {
  return date.toISOString().replace(/[:.]/g, '-')
}

async function createTaskLog(projectPath: string, taskId: DevTaskId, command: string, startedAt: string): Promise<string> {
  const dir = join(projectPath, '.echo-studio', 'logs')
  await fs.mkdir(dir, { recursive: true })
  const path = join(dir, `${logStamp(new Date(startedAt))}-${safeTaskName(taskId)}.log`)
  await fs.writeFile(path, [
    `ECHO Studio task: ${taskId}`,
    `Command: ${command}`,
    `Started: ${startedAt}`,
    ''
  ].join('\n'), 'utf8')
  return path
}

function resolveTaskLogPath(projectPath: string, logPath: string): string {
  const root = resolve(projectPath, '.echo-studio', 'logs')
  const target = resolve(logPath)
  if (target !== root && !target.startsWith(`${root}${sep}`)) {
    throw new Error('Log path must stay inside the project log folder.')
  }
  return target
}

async function appendTaskLog(logPath: string, label: string, text: string): Promise<void> {
  if (!text) return
  await fs.appendFile(logPath, `\n[${label}]\n${text}`, 'utf8')
}

async function finishTaskLog(logPath: string, status: DevTaskRun['status'], finishedAt?: string, exitCode?: number): Promise<void> {
  const lines = ['', `[status] ${status}`]
  if (exitCode !== undefined) lines.push(`[exit] ${exitCode}`)
  if (finishedAt) lines.push(`[finished] ${finishedAt}`)
  lines.push('')
  await fs.appendFile(logPath, lines.join('\n'), 'utf8')
}

function terminateChildProcess(child: ChildProcess): Promise<void> {
  if (!child.pid) {
    child.kill()
    return Promise.resolve()
  }
  if (process.platform === 'win32') {
    return new Promise((resolveTermination) => {
      const killer = spawn('taskkill', ['/PID', String(child.pid), '/T', '/F'], { windowsHide: true })
      let settled = false
      const settle = (): void => {
        if (settled) return
        settled = true
        resolveTermination()
      }
      killer.on('close', settle)
      killer.on('error', () => {
        child.kill()
        settle()
      })
    })
  }
  try {
    process.kill(-child.pid, 'SIGTERM')
  } catch {
    child.kill('SIGTERM')
  }
  return Promise.resolve()
}

async function listArtifactFiles(root: string): Promise<string[]> {
  if (!await exists(root)) return []
  const entries = await fs.readdir(root, { withFileTypes: true })
  const files: string[] = []
  for (const entry of entries) {
    const path = join(root, entry.name)
    if (entry.isDirectory()) {
      files.push(...await listArtifactFiles(path))
    } else if (entry.isFile()) {
      files.push(path)
    }
  }
  return files
}

async function collectArtifacts(projectPath: string, echoModulesRoot?: string): Promise<DevArtifact[]> {
  const roots = [
    join(projectPath, 'build', 'libs'),
    join(projectPath, 'release'),
    join(projectPath, 'exports'),
    ...(echoModulesRoot ? [join(echoModulesRoot, 'dist', 'echo-module-release')] : [])
  ]
  const artifacts: DevArtifact[] = []
  for (const root of roots) {
    for (const filePath of await listArtifactFiles(root)) {
      const stat = await fs.stat(filePath)
      artifacts.push({
        path: filePath,
        name: basename(filePath),
        kind: artifactKind(basename(filePath)),
        bytes: stat.size,
        modifiedAt: stat.mtimeMs
      })
    }
  }
  return artifacts.sort((a, b) => b.modifiedAt - a.modifiedAt)
}

async function collectCurrentArtifacts(projectPath: string): Promise<DevArtifact[]> {
  const catalog = await listEchoModules(projectPath).catch(() => undefined)
  return collectArtifacts(projectPath, catalog?.moduleRoot)
}

