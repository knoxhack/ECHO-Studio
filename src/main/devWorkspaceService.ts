function expectedModes(rel: string): DevWorkspaceMode[] {
  if (
    rel === 'echo.mod.json' ||
    rel === '.echo-studio/dev-workspace.json' ||
    rel === '.echo-studio/modules.lock.json' ||
    rel === '.echo-studio/module-workspace.json'
  ) return ['visual', 'gradle', 'full']
  if (
    [
      'settings.gradle',
      'build.gradle',
      'gradle.properties',
      'gradlew.bat',
      'gradlew',
      'src/main/resources/META-INF/echo.mod.json',
      'src/generated/resources/META-INF/echo.modules.lock.json'
    ].includes(rel) ||
    rel.startsWith('src/main/java/') ||
    rel.startsWith('src/test/java/') ||
    rel.startsWith('scripts/')
  ) {
    return ['gradle', 'full']
  }
  if (rel === 'META-INF/echo-addon-package.json' || rel === 'release' || rel === '.echo-studio/release-checklist.md') {
    return ['full']
  }
  return []
}

function isExpectedForMode(rel: string, mode: DevWorkspaceMode, runtimes: Runtime[]): boolean {
  if (rel === 'src/generated/resources/META-INF/neoforge.mods.toml') {
    return mode !== 'visual' && runtimes.includes('neoforge')
  }
  return expectedModes(rel).includes(mode)
}

async function fileStatus(projectPath: string, rel: string, mode: DevWorkspaceMode, runtimes: Runtime[]): Promise<DevWorkspaceFileStatus> {
  const path = join(projectPath, rel)
  const isThere = await exists(path)
  return {
    path: rel,
    exists: isThere,
    generatedByStudio: isThere ? (await readText(path)).includes(STUDIO_MARKER) : false,
    expected: isExpectedForMode(rel, mode, runtimes)
  }
}

export async function inspectDevWorkspace(projectPath: string): Promise<DevWorkspaceState> {
  const manifest = await readManifest(projectPath)
  if (!manifest) throw new Error('Missing echo.mod.json')
  const moduleCatalog = await listEchoModules(projectPath)
  const studioStatePath = join(projectPath, '.echo-studio', 'dev-workspace.json')
  const saved = await readText(studioStatePath)
  const parsed = saved ? JSON.parse(saved) as Partial<DevWorkspaceState> : {}
  const mode = parsed.mode ?? 'visual'
  const runtimes = parsed.runtimeTargets ?? manifest.runtime.supports
  const javaPath = `src/main/java/${packagePath(manifest)}/${className(manifest)}.java`
  const testPath = `src/test/java/${packagePath(manifest)}/${className(manifest)}Test.java`
  const files = await Promise.all([
    fileStatus(projectPath, 'echo.mod.json', mode, runtimes),
    fileStatus(projectPath, '.echo-studio/dev-workspace.json', mode, runtimes),
    fileStatus(projectPath, '.echo-studio/modules.lock.json', mode, runtimes),
    fileStatus(projectPath, '.echo-studio/module-workspace.json', mode, runtimes),
    fileStatus(projectPath, 'META-INF/echo-addon-package.json', mode, runtimes),
    fileStatus(projectPath, 'settings.gradle', mode, runtimes),
    fileStatus(projectPath, 'build.gradle', mode, runtimes),
    fileStatus(projectPath, 'gradle.properties', mode, runtimes),
    fileStatus(projectPath, 'gradlew.bat', mode, runtimes),
    fileStatus(projectPath, 'gradlew', mode, runtimes),
    fileStatus(projectPath, 'src/main/resources/META-INF/echo.mod.json', mode, runtimes),
    fileStatus(projectPath, 'src/generated/resources/META-INF/echo.modules.lock.json', mode, runtimes),
    fileStatus(projectPath, 'src/generated/resources/META-INF/neoforge.mods.toml', mode, runtimes),
    fileStatus(projectPath, javaPath, mode, runtimes),
    fileStatus(projectPath, testPath, mode, runtimes),
    fileStatus(projectPath, 'scripts/run-dev-client.ps1', mode, runtimes),
    fileStatus(projectPath, 'scripts/build-local.ps1', mode, runtimes),
    fileStatus(projectPath, '.echo-studio/release-checklist.md', mode, runtimes),
    fileStatus(projectPath, 'release', mode, runtimes)
  ])
  const modulePlan = resolveProjectModulePlan(manifest, moduleCatalog.catalog)
  const hasGradleWrapper = files.some((file) => ['gradlew.bat', 'gradlew'].includes(file.path) && file.exists)
  const gradleReady = files.some((file) => file.path === 'build.gradle' && file.exists) && files.some((file) => file.path === 'settings.gradle' && file.exists)
  const sourceReady = files.some((file) => file.path.startsWith('src/main/java') && file.exists)
  const expectedReady = files.filter((file) => file.expected).every((file) => file.exists)
  const lockStatus = await moduleLockStatus(projectPath, manifest, modulePlan, mode)
  const moduleWorkspace = await moduleWorkspaceStatus(projectPath, manifest, modulePlan)
  const runtimeLaunchers = await runtimeLauncherStatus(projectPath, runtimes, mode)
  const toolchain = await toolchainStatus(projectPath, hasGradleWrapper)
  return {
    ready: Boolean(parsed.lastSetupAt) && expectedReady && lockStatus.upToDate && moduleWorkspace.upToDate,
    mode,
    projectPath,
    gradleReady,
    hasGradleWrapper,
    sourceReady,
    runtimeTargets: runtimes,
    files,
    toolchain,
    modulePlan,
    moduleCatalog: moduleCatalogStatus(moduleCatalog),
    moduleWorkspace,
    moduleLock: lockStatus,
    runtimeLaunchers,
    artifacts: await collectArtifacts(projectPath, moduleCatalog.moduleRoot),
    lastSetupAt: parsed.lastSetupAt
  }
}

export async function setupDevWorkspace(projectPath: string, options: DevWorkspaceOptions): Promise<DevSetupResult> {
  const manifest = await readManifest(projectPath)
  if (!manifest) throw new Error('Missing echo.mod.json')
  const runtimes = runtimeTargets(manifest, options)
  const moduleCatalog = await listEchoModules(projectPath)
  const moduleLock = buildModuleLock(manifest, moduleCatalog)
  const moduleLockJson = `${JSON.stringify(moduleLock, null, 2)}\n`
  const moduleWorkspace = buildModuleWorkspace(manifest, moduleCatalog)
  const moduleWorkspaceJson = `${JSON.stringify(moduleWorkspace, null, 2)}\n`
  const written: string[] = []
  const skipped: string[] = []
  const force = Boolean(options.force)

  await fs.mkdir(join(projectPath, '.echo-studio'), { recursive: true })
  await writeGenerated(join(projectPath, '.echo-studio', 'modules.lock.json'), moduleLockJson, written)
  await writeGenerated(join(projectPath, '.echo-studio', 'module-workspace.json'), moduleWorkspaceJson, written)

  if (options.mode === 'gradle' || options.mode === 'full') {
    await writeIfAllowed(join(projectPath, 'settings.gradle'), settingsGradle(manifest), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'gradle.properties'), gradleProperties(manifest, runtimes, options), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'build.gradle'), buildGradle(), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'gradlew.bat'), gradlewBat(), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'gradlew'), gradlewSh(), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'src', 'main', 'resources', 'META-INF', 'echo.mod.json'), JSON.stringify(manifest, null, 2), force, written, skipped)
    await writeGenerated(join(projectPath, 'src', 'generated', 'resources', 'META-INF', 'echo.modules.lock.json'), moduleLockJson, written)
    if (runtimes.includes('neoforge')) {
      await writeGenerated(join(projectPath, 'src', 'generated', 'resources', 'META-INF', 'neoforge.mods.toml'), buildNeoForgeModsToml(manifest), written)
    }
    await writeIfAllowed(join(projectPath, 'src', 'main', 'java', ...packagePath(manifest).split('/'), `${className(manifest)}.java`), javaSource(manifest), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'src', 'test', 'java', ...packagePath(manifest).split('/'), `${className(manifest)}Test.java`), testSource(manifest), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'scripts', 'run-dev-client.ps1'), runScript('run-dev-client', manifest), force, written, skipped)
    await writeIfAllowed(join(projectPath, 'scripts', 'build-local.ps1'), runScript('build-local', manifest), force, written, skipped)
  }

  if (options.mode === 'full') {
    await writeIfAllowed(
      join(projectPath, 'META-INF', 'echo-addon-package.json'),
      JSON.stringify(buildAddonPackageManifest(manifest, moduleCatalog.catalog), null, 2),
      force,
      written,
      skipped
    )
    const releaseDir = join(projectPath, 'release')
    const releaseExists = await exists(releaseDir)
    await fs.mkdir(releaseDir, { recursive: true })
    if (releaseExists && !force) skipped.push(releaseDir)
    else written.push(releaseDir)
    await writeIfAllowed(join(projectPath, '.echo-studio', 'release-checklist.md'), releaseChecklist(manifest), force, written, skipped)
  }

  const statePath = join(projectPath, '.echo-studio', 'dev-workspace.json')
  await fs.writeFile(statePath, JSON.stringify({
    mode: options.mode,
    runtimeTargets: runtimes,
    lastSetupAt: new Date().toISOString()
  }, null, 2), 'utf8')
  written.push(statePath)

  return {
    state: await inspectDevWorkspace(projectPath),
    written: written.map((path) => relative(projectPath, path)),
    skipped: skipped.map((path) => relative(projectPath, path))
  }
}

function gradleCommand(projectPath: string): string {
  if (process.platform === 'win32' && existsSync(join(projectPath, 'gradlew.bat'))) return '.\\gradlew.bat'
  if (existsSync(join(projectPath, 'gradlew'))) return './gradlew'
  return 'gradle'
}

function runShell(command: string, cwd: string, timeoutMs: number): Promise<{ exitCode: number; stdout: string; stderr: string }> {
  return new Promise((resolve) => {
    const child = spawn(command, {
      cwd,
      shell: true,
      windowsHide: true
    })
    let stdout = ''
    let stderr = ''
    const timeout = setTimeout(() => {
      child.kill()
      stderr += `\nCommand timed out after ${timeoutMs}ms.`
    }, timeoutMs)
    child.stdout?.on('data', (chunk) => { stdout += String(chunk) })
    child.stderr?.on('data', (chunk) => { stderr += String(chunk) })
    child.on('close', (code) => {
      clearTimeout(timeout)
      resolve({ exitCode: code ?? 0, stdout, stderr })
    })
    child.on('error', (error) => {
      clearTimeout(timeout)
      resolve({ exitCode: 1, stdout, stderr: `${stderr}\n${error.message}` })
    })
  })
}

function isEchoModulesTask(taskId: DevTaskId): boolean {
  return taskId.startsWith('modules:')
}

function selectedLocalModuleIds(plan: ProjectModulePlan): string[] {
  return sortedUnique(
    plan.closure
      .filter((mod) => mod.moduleDir || mod.descriptorPath)
      .map((mod) => mod.id)
  )
}

function assertSafeModuleTaskId(moduleId: string): string {
  if (!/^[A-Za-z0-9._-]+$/.test(moduleId)) throw new Error(`Unsafe ECHO module id for task command: ${moduleId}`)
  return moduleId
}

async function echoModulesTaskCommand(projectPath: string, taskId: DevTaskId): Promise<{ command: string; cwd: string }> {
  const catalog = await listEchoModules(projectPath)
  if (!catalog.moduleRoot) throw new Error('Local ECHO-Modules metadata/modules/index.json was not found.')

  const script = (name: string): string => join(catalog.moduleRoot!, 'scripts', name)
  const requireScript = async (name: string): Promise<void> => {
    if (!await exists(script(name))) throw new Error(`Local ECHO-Modules scripts/${name} was not found.`)
  }

  if (taskId === 'modules:validate') {
    await requireScript('validate-module-graph.mjs')
    return { command: 'node scripts/validate-module-graph.mjs', cwd: catalog.moduleRoot }
  }
  if (taskId === 'modules:docsAudit') {
    await requireScript('docs-audit.mjs')
    return { command: 'node scripts/docs-audit.mjs', cwd: catalog.moduleRoot }
  }
  if (taskId === 'modules:verifyRelease') {
    await requireScript('verify-module-release.mjs')
    return { command: 'node scripts/verify-module-release.mjs --release-dir dist/echo-module-release', cwd: catalog.moduleRoot }
  }

  if (taskId === 'modules:releaseSelected' || taskId === 'modules:releaseAll') {
    await requireScript('generate-module-release.mjs')
    const args = ['node scripts/generate-module-release.mjs --out dist/echo-module-release --package-from-source']
    if (taskId === 'modules:releaseSelected') {
      const manifest = await readManifest(projectPath)
      if (!manifest) throw new Error('Missing echo.mod.json')
      const selected = selectedLocalModuleIds(resolveProjectModulePlan(manifest, catalog.catalog))
      if (selected.length === 0) {
        throw new Error('No resolved modules are linked to local ECHO-Modules source.')
      }
      for (const moduleId of selected) args.push(`--module ${assertSafeModuleTaskId(moduleId)}`)
    }
    return { command: args.join(' '), cwd: catalog.moduleRoot }
  }

  throw new Error(`Unsupported ECHO-Modules task: ${taskId}`)
}

export async function runDevTask(projectPath: string, taskId: DevTaskId): Promise<DevTaskRun> {
  const task = DEV_TASKS.find((item) => item.id === taskId)
  if (!task) throw new Error(`Unknown dev task: ${taskId}`)
  const startedAt = new Date().toISOString()
  await assertDevTaskReady(projectPath, task)

  if (isEchoModulesTask(task.id)) {
    const { command, cwd } = await echoModulesTaskCommand(projectPath, task.id)
    const logPath = await createTaskLog(projectPath, taskId, command, startedAt)
    const result = await runShell(command, cwd, 180000)
    const status = result.exitCode === 0 ? 'completed' : 'failed'
    const finishedAt = new Date().toISOString()
    await appendTaskLog(logPath, 'stdout', result.stdout)
    await appendTaskLog(logPath, 'stderr', result.stderr)
    await finishTaskLog(logPath, status, finishedAt, result.exitCode)
    return {
      taskId,
      status,
      command,
      cwd,
      logPath,
      exitCode: result.exitCode,
      stdout: result.stdout,
      stderr: result.stderr,
      startedAt,
      finishedAt,
      artifacts: await collectArtifacts(projectPath, cwd)
    }
  }

  if (task.id === 'package:local') {
    const command = 'ECHO Studio packageAddon'
    const logPath = await createTaskLog(projectPath, taskId, command, startedAt)
    let result: Awaited<ReturnType<typeof packageAddon>>
    try {
      result = await packageAddon(projectPath, await inspectDevWorkspace(projectPath))
    } catch (error) {
      const finishedAt = new Date().toISOString()
      await appendTaskLog(logPath, 'error', error instanceof Error ? error.message : String(error))
      await finishTaskLog(logPath, 'failed', finishedAt)
      throw error
    }
    const stdout = `Prepared ${basename(result.zipPath)} with ${result.assetPaths.length} release asset(s).`
    const stderr = result.sdkValidation.issues.join('\n')
    const finishedAt = new Date().toISOString()
    await appendTaskLog(logPath, 'stdout', stdout)
    await appendTaskLog(logPath, 'stderr', stderr)
    await finishTaskLog(logPath, 'completed', finishedAt, 0)
    return {
      taskId,
      status: 'completed',
      command,
      cwd: projectPath,
      logPath,
      stdout,
      stderr,
      startedAt,
      finishedAt,
      artifacts: await collectCurrentArtifacts(projectPath)
    }
  }

  const command = `${gradleCommand(projectPath)} ${task.command}`
  const logPath = await createTaskLog(projectPath, taskId, command, startedAt)
  if (task.detached) {
    const taskLogPath = resolveTaskLogPath(projectPath, logPath)
    const child = spawn(command, {
      cwd: projectPath,
      shell: true,
      detached: true,
      stdio: ['ignore', 'pipe', 'pipe'],
      windowsHide: true
    })
    const running: RunningDevProcess = {
      child,
      taskId,
      command,
      cwd: projectPath,
      logPath: taskLogPath,
      startedAt,
      stopping: false
    }
    runningDevProcesses.set(taskLogPath, running)
    child.stdout?.on('data', (chunk) => { void appendTaskLog(logPath, 'stdout', String(chunk)) })
    child.stderr?.on('data', (chunk) => { void appendTaskLog(logPath, 'stderr', String(chunk)) })
    child.on('close', (code) => {
      const current = runningDevProcesses.get(taskLogPath)
      if (current === running) runningDevProcesses.delete(taskLogPath)
      if (!running.stopping) {
        void finishTaskLog(logPath, code === 0 ? 'completed' : 'failed', new Date().toISOString(), code ?? undefined)
      }
    })
    child.on('error', (error) => {
      const current = runningDevProcesses.get(taskLogPath)
      if (current === running) runningDevProcesses.delete(taskLogPath)
      void appendTaskLog(logPath, 'error', error.message)
      if (!running.stopping) void finishTaskLog(logPath, 'failed', new Date().toISOString())
    })
    child.unref()
    return {
      taskId,
      status: 'started',
      command,
      cwd: projectPath,
      pid: child.pid,
      logPath,
      stdout: `Started ${task.label} as process ${child.pid}.`,
      stderr: '',
      startedAt,
      artifacts: await collectCurrentArtifacts(projectPath)
    }
  }

  const result = await runShell(command, projectPath, 180000)
  const status = result.exitCode === 0 ? 'completed' : 'failed'
  const finishedAt = new Date().toISOString()
  await appendTaskLog(logPath, 'stdout', result.stdout)
  await appendTaskLog(logPath, 'stderr', result.stderr)
  await finishTaskLog(logPath, status, finishedAt, result.exitCode)
  return {
    taskId,
    status,
    command,
    cwd: projectPath,
    logPath,
    exitCode: result.exitCode,
    stdout: result.stdout,
    stderr: result.stderr,
    startedAt,
    finishedAt,
    artifacts: await collectCurrentArtifacts(projectPath)
  }
}

export async function listRunningDevTasks(projectPath: string): Promise<DevTaskRun[]> {
  const projectRoot = resolve(projectPath)
  const artifacts = await collectCurrentArtifacts(projectPath)
  return [...runningDevProcesses.values()]
    .filter((running) => resolve(running.cwd) === projectRoot)
    .sort((a, b) => b.startedAt.localeCompare(a.startedAt))
    .map((running) => ({
      taskId: running.taskId,
      status: 'started',
      command: running.command,
      cwd: running.cwd,
      ...(running.child.pid ? { pid: running.child.pid } : {}),
      logPath: running.logPath,
      stdout: `Started ${running.taskId} as process ${running.child.pid}.`,
      stderr: '',
      startedAt: running.startedAt,
      artifacts
    }))
}

export async function stopDevTask(projectPath: string, logPath: string): Promise<DevTaskStopResult> {
  const taskLogPath = resolveTaskLogPath(projectPath, logPath)
  const running = runningDevProcesses.get(taskLogPath)
  if (!running) {
    return {
      status: 'not_running',
      logPath: taskLogPath,
      message: 'No running dev task was found for this log.'
    }
  }
  running.stopping = true
  runningDevProcesses.delete(taskLogPath)
  const finishedAt = new Date().toISOString()
  await appendTaskLog(taskLogPath, 'stop', 'Stop requested from ECHO Studio.')
  await terminateChildProcess(running.child)
  await finishTaskLog(taskLogPath, 'stopped', finishedAt)
  return {
    status: 'stopped',
    taskId: running.taskId,
    command: running.command,
    cwd: running.cwd,
    ...(running.child.pid ? { pid: running.child.pid } : {}),
    logPath: taskLogPath,
    finishedAt,
    message: `Stopped ${running.taskId}.`
  }
}

export async function readDevTaskLog(projectPath: string, logPath: string): Promise<string> {
  const target = resolveTaskLogPath(projectPath, logPath)
  const text = await fs.readFile(target, 'utf8')
  return text.length > 200000 ? text.slice(-200000) : text
}
